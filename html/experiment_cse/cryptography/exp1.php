<h3>About the experiment:</h3>
<ul style="list-style-image: url('http://www.amifs.org/new_images/home/list-bullets2.gif');">
<li>Apparently, the system is easily broken if the total number of distinct secret keys is small, that is the key space K is small.<br>
</br>
<li>In this experiment, we work with a well-known historical encryption scheme, namely the shift cipher, that has a very small key space.<br>
</br>
<li>Your task is to break the shift cipher. Specifically, given (only) the ciphertext in some instance of a shift cipher, you need to find the plaintext and the secret key.
