(a) Gen<br/>
(b) Enc<br/>
(c) Dec<br/>
</br>
<li>The algorithm for key generation Gen is used to choose a key k at random from the set of all possible secert keys, denoted by the key space K.<br/>
</br>
<li>The algorithm for encryption Enc takes as inputs the message m and the secret key k and outputs the ciphertext c.<br/>
</br>
<li>The algorithm for decryption Dec inputs the ciphertext c and the key k and outputs the message m.</ul>
