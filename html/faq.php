<strong>            5.	 	What are the various types of Virtual Labs?</strong></a>
<p> There are two types of Virtual Labs: </p>
<br>
<p>
 	 	<strong>Simulation  Based Virtual Labs:</strong>&nbsp; In these Virtual Labs, the  experiments are modeled using mathematical equations.&nbsp; The simulations are carried out remotely at a  high end server, and the results are communicated to the student over the  internet.&nbsp; This class of Virtual Labs, at  best, mimics the real-world scenarios/experiments.&nbsp; Simulation based Virtual Labs are scalable  and can cater to a large number of simultaneous users.
</p>
<p>
<strong>Remote  Triggered Virtual Labs: </strong>In these Virtual Labs, the actual experiments  are triggered remotely.&nbsp; The output of the  experiment (being conducted remotely) is communicated back to the student over  the internet.&nbsp; This class of Virtual  Labs, gives the student the output of real-time experiments.&nbsp; Remote Triggered Virtual Labs are difficult  to scale and can cater to a limited number of users.&nbsp; Typically, time-slots are booked before  conducting such experiments.
</p>
<br>
<strong> 6. When can we use the Virtual Labs? </strong>
		<p>
Virtual Labs are divided into two categories, namely remote triggered labs and the simulation based labs.  For the remote triggered labs, the user can use them as per the time mentioned on the website.  The simulation based labs are available 24x7 including weekends.
</p>
<br>
<strong>7. What do I do if I am stuck?</strong>
		<p>
The users can email their queries to the virtual labs support team at support@vlab.co.in.  Also, one can call directly on the hotline number 011-64674687 for help.
</p>
<br>
<strong>8.. How do I register for using the Virtual Labs? </strong>

<p> As mentioned earlier, Virtual Labs are divided into two categories: simulation based labs and remote triggered labs. Registration requirements are different for the two categories. </p>
<ol type="A">
                    <li><strong>Simulation based Labs:</strong>  Many of the simulation based labs do not require registration. You
					 can directly perform the experiment without registering yourself. On the other hand, some of the simulation 
					 virtual labs require registration for which the user can go to the respective website do a one-time 
					 registration.<br> <br>
                    </li>
                  <li><strong>Remote Triggered Labs: </strong>To access a remote triggered lab, a user has to book a slot. For 
				  booking a slot, you have to register yourself first and then book a slot from the slot chart.  All the available 
				  /booked slots can be viewed on this chart.  Once a request for slot-booking is made, all the information 
				  regarding the user id, password and the slot booked will be sent to the user’s email address provided by the user
				  at the time of registration. Once registered, a user can login using the same credentials every time. This 
				  registration has to be done by the user separately for each of the remote triggered virtual labs.				  </li>
</ol>
<br>

