           <strong>1.   How can I access the Virtual Labs? </strong>

		<p>
All Virtual Labs can be accessed through a common website: <a href="http://ui2.virtual-labs.ac.in" target="_blank">link</a> . At the user end, a PC and broadband connectivity enables the user to access Virtual Labs.
